# COMANDO APPEND CRIA A LISTA DE FORMA CRESCENTE COM fro i in range (n)
my_list = []  # Creating an empty list.

for i in range(5):
    my_list.append(i + 1)

print(my_list)

# COMANDO INSERT CRIA A LISTA DE FORMA DECRESCENTE /COM for i in range (n)

my_list = []  # Creating an empty list.

for i in range(5):
    my_list.insert(0, i + 1)

print(my_list)

